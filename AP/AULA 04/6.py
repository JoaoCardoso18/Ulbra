n1 = float(input('Informe o primeiro número: '))
n2 = float(input('Informe o segundo número: '))
n3 = float(input('Informe o terceiro número: '))

m = n1 * n2 * n3
s = n1 + n2 + n3
sub = n1 - n2 - n3
st = m + s + sub

print('A multiplicação dos números é {}, a soma {}, a subtraçao {}, e a soma de todos os resultados é {}'.format(m, s, sub, st))